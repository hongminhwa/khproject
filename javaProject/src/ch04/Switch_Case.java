package ch04;

public class Switch_Case {

		public static void main(String[] args) {
			 int i = 0; i++;  
			 switch(i) {
			 case 1:
			        System.out.println("i의 값은 : 0");
			 case 2: 
				 
				 System.out.println("i의 값은 : 1");
				 
			 case 3:  // 값 일치 
				 System.out.println("i의 값은 : 2");
				 
				 
			 case 4: 
				 System.out.println("i의 값은 : 3");
				 
		     default: 
		    	 System.out.println("i의 값은 : 4 ");
			 }
		}

	

	}


