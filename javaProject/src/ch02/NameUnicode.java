package ch02;

public class NameUnicode {

	public static void main(String[] args) { // char => 문자타입 
		char h = '\ud64d';
		char m =  '\ubbfc';
		char w =  '\ud654'; 
		
		 
		System.out.println("char형(\'\\ud64d\')" + h );
		System.out.println("char형(\'\\ubbfc\')" + m);
		System.out.println("char형(\'\\ud654\')" + w);
		
	}

}
