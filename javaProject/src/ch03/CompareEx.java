package ch03;

public class CompareEx {
	 public static void main(String[] args) {
		
		 
		 int a = 65; 
		 char c = 'A';
		 System.out.println(a==c);/////  비교 연산자.  좌변과 우변이 같은지 비교. 
		 System.out.println( a = c); //  대입연산자. 
		 System.out.println(c != 65);
		 System.out.println(a <= c); 
		 
		 
				
		 
	}
}
