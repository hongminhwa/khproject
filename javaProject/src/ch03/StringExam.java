package ch03;

public class StringExam {

	public static void main(String[] args) {
		String str = "Hello" + "java";  // 문자열을 연결 시킴.
		System.out.println(str);
		str = 123 + "C";
		System.out.println(str);  // 숫자열 + 문자열 
		
		str += "Programmer"; // str = str + "programmer" 
		System.out.println(str);
           		
		
		
		

	}

}
