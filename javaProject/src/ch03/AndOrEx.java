package ch03;

public class AndOrEx {

	public static void main(String[] args) {
		   
    int a = 7;  
     // 논리 연산자 && || 
    System.out.println( a > 5 && a < 0); // T&&F => f 한개의 false가 있으면 무조건 false
    System.out.println( a > 5 || a < 0);   // T || F  =>  한개의 true가 있으면 true.
    
     
	}

}
